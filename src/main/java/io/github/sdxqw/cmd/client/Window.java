package io.github.sdxqw.cmd.client;

/**
 * The Window class contains static fields that define the size and handle of the game window.
 */
public class Window {

    /**
     * The width of the game window.
     */
    public static int width = 820;

    /**
     * The height of the game window.
     */
    public static int height = 420;

    /**
     * The handle of the game window, represented as a long value.
     */
    public static long window = 0L;

    /**
     * The handle of the NanoVG graphics context, represented as a long value.
     */
    public static long nvg = 0L;
}
